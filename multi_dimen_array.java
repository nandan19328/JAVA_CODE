public class multi_dimen_array {
    
        public static void main(String[] args)
        {
           
            int[][] arr = new int[4][13];
           
    
           
            System.out.println("Number of Rows:"+
                               arr.length);
          
              
            System.out.println("Number of Columns:"+
                               arr[0].length);
        }
    }
    

